export type TranslationKeys = typeof translations.en;

type PathsToLeaves<T, Prefix extends string = ""> = {
  [K in keyof T]: T[K] extends Record<string, string>
    ? `${Prefix}${K & string}.${keyof T[K] & string}`
    : never;
}[keyof T];

export type TKey = PathsToLeaves<TranslationKeys>;

export function getTranslations(locale: Locale) {
  const dict = translations[locale];
  return function t(key: TKey, vars?: Record<string, string>): string {
    const parts = key.split(".");
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    let value: any = dict;
    for (const part of parts) {
      value = value?.[part];
    }
    let result = (value as string) ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        result = result.replace(`{{${k}}}`, v);
      }
    }
    return result;
  };
}
